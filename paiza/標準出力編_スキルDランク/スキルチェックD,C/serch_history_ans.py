n = int(input())

re = []

for i in range(n):
    s = input()
    if s in re:
        re.pop(re.index(s))#暫定、既出で入ってるリストの場所を参照し、削除する
    re.append(s)#また末尾に入れなおす
for i in range(len(re)):
    print(re[-i - 1])#これで逆順出力らしい、んなもん知るか
"""
    このPythonのコードは、ユーザーが入力したテキストを処理して、重複を削除し、逆順で表示するものです。それぞれの部分を説明しますね：

n = int(input()): ユーザーから整数 n を入力します。これはリストの要素数を指定します。
re = []: 空のリスト re を作成します。ここにユーザーが入力した文字列を格納します。
for i in range(n):: n 回繰り返すループを開始します。ユーザーが指定した回数だけ以下の処理を繰り返します。
s = input(): ユーザーから文字列 s を入力します。
if s in re:: もし s がリスト re に既に含まれている場合、次の処理を行います。
re.pop(re.index(s)): re リストから s の最初の出現位置を探し、その要素を削除します。
re.append(s): リスト re の末尾に s を追加します。
for i in range(len(re)):: re リストの要素数回繰り返すループを開始します。ユーザーが入力した文字列を逆順で表示します。
print(re[-i - 1]): リスト re の末尾から i + 1 番目の要素を表示します。
最後の行 print(re[-i - 1]) は、ループの外で実行されているため、最後にリスト re の最初の要素を表示しています。
"""